fn discoverPackages(allocator: std.mem.Allocator, query: ?[]const u8) !void {
    std.debug.print("ğŸ” Discovering packages via Zigistry...\n", .{});
    
    var zigistry_client = zigistry.ZigistryClient.init(allocator, null);
    defer zigistry_client.deinit();

    const search_query = query orelse "";
    const packages = try zigistry_client.searchPackages(search_query, 10);
    defer {
        for (packages) |*pkg| {
            pkg.deinit(allocator);
        }
        allocator.free(packages);
    }

    if (packages.len == 0) {
        std.debug.print("ğŸ¤· No packages found for query: '{s}'\n", .{search_query});
        return;
    }

    std.debug.print("\nğŸ“¦ Found {} packages:\n\n", .{packages.len});

    for (packages) |pkg| {
        std.debug.print("ğŸ”¸ {s}\n", .{pkg.name});
        if (pkg.description) |desc| {
            std.debug.print("   {s}\n", .{desc});
        }
        std.debug.print("   â­ {} stars | ğŸ“Š Score: {d:.2}\n", .{ pkg.github_stars, pkg.zigistry_score });
        std.debug.print("   ğŸ”— {s}\n", .{pkg.github_url});
        
        if (pkg.topics.len > 0) {
            std.debug.print("   ğŸ·ï¸  Topics: ", .{});
            for (pkg.topics, 0..) |topic, i| {
                if (i > 0) std.debug.print(", ", .{});
                std.debug.print("{s}", .{topic});
            }
            std.debug.print("\n", .{});
        }
        std.debug.print("\n", .{});
    }

    std.debug.print("ğŸ’¡ To add a package: zepplin add <package-name>\n", .{});
}

